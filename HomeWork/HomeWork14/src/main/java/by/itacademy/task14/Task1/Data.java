public class Data {
    private String text;
    private int number;
    private float field;

    public Data() {
        this.text = text;
        this.number = number;
        this.field = field;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public float getField() {
        return field;
    }

    public void setField(float field) {
        this.field = field;
    }
}
